// Функция поиска ответа
window.AI_KNOWLEDGE.findResponse = function(message) {
    const lowerMsg = message.toLowerCase();
    
    // Проверяем все категории
    const categories = ['greetings', 'aboutAI', 'capabilities', 'farewells'];
    
    for (const category of categories) {
        const responses = window.AI_KNOWLEDGE[category] || window.AI_KNOWLEDGE.generalResponses[category];
        if (!responses) continue;
        
        for (const item of responses) {
            for (const keyword of item.keywords) {
                if (lowerMsg.includes(keyword)) {
                    return {
                        found: true,
                        response: item.response,
                        category: category
                    };
                }
            }
        }
    }
    
    return { found: false };
};

