// Функция поиска проблемы
window.AI_KNOWLEDGE.findTroubleshooting = function(message) {
    const lowerMsg = message.toLowerCase();
    
    for (const issue of window.AI_KNOWLEDGE.troubleshooting) {
        // Проверяем название проблемы
        if (lowerMsg.includes(issue.problem.toLowerCase())) {
            return issue;
        }
        
        // Проверяем синонимы
        for (const synonym of issue.synonyms) {
            if (lowerMsg.includes(synonym.toLowerCase())) {
                return issue;
            }
        }
    }
    
    return null;
};

