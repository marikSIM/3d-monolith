    search: function(query) {
        const lower = query.toLowerCase();
        
        // 1. Проверка быстрых ответов
        for (const [key, answer] of Object.entries(this.quickAnswers)) {
            if (lower.includes(key)) {
                return answer;
            }
        }
        
        // 2. Проверка основных разделов
        const sections = [
            this.definition,
            this.values,
            this.influence,
            this.printTime,
            this.nozzleRule,
            this.firstLayer,
            this.recommendations,
            this.mistakes
        ];
        
        for (const section of sections) {
            if (section.question) {
                for (const q of section.question) {
                    if (lower.includes(q) || q.includes(lower.split(' ')[0])) {
                        return section.answer;
                    }
                }
            }
        }
        
        // 3. Поиск по ключевым словам
        const keywords = {
            'слой': this.definition.answer,
            'высота': this.definition.answer,
            'толщина': this.definition.answer,
            '0.1': this.values.answer,
            '0.2': this.values.answer,
            '0.3': this.values.answer,
            'влия': this.influence.answer,
            'время': this.printTime.answer,
            'сопло': this.nozzleRule.answer,
            'первый': this.firstLayer.answer,
            'рекоменд': this.recommendations.answer,
            'ошибк': this.mistakes.answer,
            'проблем': this.mistakes.answer
        };
        
        for (const [key, answer] of Object.entries(keywords)) {
            if (lower.includes(key)) {
                return answer;
            }
        }
        
        return null;
    }
