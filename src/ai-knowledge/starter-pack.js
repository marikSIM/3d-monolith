// Функция нечёткого сравнения (fuzzy match)
window.AI_STARTER_KNOWLEDGE.fuzzyMatch = function(str1, str2, threshold) {
    threshold = threshold || 0.7;
    str1 = str1.toLowerCase().replace(/[^а-яa-z0-9]/g, '');
    str2 = str2.toLowerCase().replace(/[^а-яa-z0-9]/g, '');
    
    if (str1 === str2) return 1.0;
    if (str1.includes(str2) || str2.includes(str1)) return 0.9;
    
    // Простое сравнение по общим буквам
    const set1 = new Set(str1.split(''));
    const set2 = new Set(str2.split(''));
    const intersection = new Set([...set1].filter(x => set2.has(x)));
    const union = new Set([...set1, ...set2]);
    
    return intersection.size / union.size;
};

// Функция поиска в стартовом пакете
window.AI_STARTER_KNOWLEDGE.search = function(query) {
    const q = query.toLowerCase().trim();
    const self = this;
    
    // 1. Точное совпадение по категориям
    for (const category of Object.values(this)) {
        if (category && typeof category === 'object') {
            for (const [key, response] of Object.entries(category)) {
                if (key === 'search' || key === 'fuzzyMatch') continue;
                if (typeof response === 'string' && q.includes(key)) {
                    return response;
                }
                // Массив ответов (для диалогов)
                if (Array.isArray(response)) {
                    for (const item of response) {
                        if (item.q && Array.isArray(item.q) && item.q.some(k => q.includes(k))) {
                            return item.a || item.response;
                        }
                    }
                }
            }
        }
    }
    
    // 2. Нечёткий поиск по ключевым словам
    const allKeywords = [];
    for (const category of Object.values(this)) {
        if (category && typeof category === 'object') {
            for (const key of Object.keys(category)) {
                if (key !== 'search' && key !== 'fuzzyMatch') {
                    allKeywords.push(key);
                }
            }
        }
    }
    
    const bestMatch = allKeywords
        .map(k => ({ key: k, score: self.fuzzyMatch(q, k, 0.6) }))
        .filter(m => m.score > 0.6)
        .sort((a, b) => b.score - a.score)[0];
    
    if (bestMatch && bestMatch.score >= 0.7) {
        for (const category of Object.values(this)) {
            if (category && typeof category === 'object' && category[bestMatch.key]) {
                return typeof category[bestMatch.key] === 'string' ? category[bestMatch.key] : null;
            }
        }
    }
    
    // 3. Поиск по ключевым словам в keywords
    if (this.keywords) {
        for (const [key, response] of Object.entries(this.keywords)) {
            if (q.includes(key)) {
                return response;
            }
        }
    }
    
    return null;
};

