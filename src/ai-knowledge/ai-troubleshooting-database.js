    search: function(query) {
        const q = query.toLowerCase();
        const results = [];
        
        // Поиск в FDM
        for (const problem of this.fdm) {
            if (problem.keywords.some(k => q.includes(k)) ||
                problem.name.toLowerCase().includes(q) ||
                problem.symptoms.some(s => q.includes(s.toLowerCase()))) {
                results.push({ type: 'FDM', ...problem });
            }
        }
        
        // Поиск в SLA
        for (const problem of this.sla) {
            if (problem.keywords.some(k => q.includes(k)) ||
                problem.name.toLowerCase().includes(q) ||
                problem.symptoms.some(s => q.includes(s.toLowerCase()))) {
                results.push({ type: 'SLA', ...problem });
            }
        }
        
        return results;
    },
    
    // === ФУНКЦИЯ РЕКОМЕНДАЦИИ ===
    getSolution: function(problemId) {
        for (const problem of [...this.fdm, ...this.sla]) {
            if (problem.id === problemId) {
                return problem;
            }
        }
        return null;
    }
